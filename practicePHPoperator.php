<?php 
	$num1 = 100;

	$num2 = 50;


	// Addition
	echo $num1 . " + " . $num2 . " = " .$num1 + $num2 . "<br>";
	// Subtraction
	echo $num1 . " - " . $num2 . " = " .$num1 - $num2 . "<br>";

	// Multiplication
	echo $num1 . " * " . $num2 . " = " .$num1 * $num2 . "<br>";
	// Division
	echo $num1 . " / " . $num2 . " = " .$num1 / $num2 . "<br>";
	// Modulus
	echo $num1 . " % " . $num2 . " = " .$num1 % $num2 . "<br>";

	// Exponentiation
	echo $num1 . " ^ " . $num2 . " = " .$num1 ** $num2 . "<br>";


?>